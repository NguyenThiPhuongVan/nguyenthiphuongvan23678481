a= int(input("Nhập số thứ nhất: "))
b= int(input("Nhập số thứ hai: "))
c= int(input("Nhập số thứ ba: "))

m_max=max (a,b,c)
m_min=min (a,b,c)

print(f"số lớn nhất là: {m_max}")

print(f"số nhỏ nhất là: {m_min}")

      

